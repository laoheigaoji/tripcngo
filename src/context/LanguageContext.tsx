// 检测浏览器语言
const detectBrowserLanguage = (): Language => {
  const browserLang = navigator.language.toLowerCase();
  
  // 中文相关语言
  if (browserLang.startsWith('zh')) {
    return 'zh';
  }
  // 日语
  if (browserLang.startsWith('ja')) {
    return 'ja';
  }
  // 韩语
  if (browserLang.startsWith('ko')) {
    return 'ko';
  }
  // 俄语
  if (browserLang.startsWith('ru')) {
    return 'ru';
  }
  // 法语
  if (browserLang.startsWith('fr')) {
    return 'fr';
  }
  // 西班牙语
  if (browserLang.startsWith('es')) {
    return 'es';
  }
  // 德语
  if (browserLang.startsWith('de')) {
    return 'de';
  }
  // 意大利语
  if (browserLang.startsWith('it')) {
    return 'it';
  }
  // 其他语言默认英文
  return 'en';
};

// 检测 IP 所属国家
const detectIPCountry = async (): Promise<string | null> => {
  try {
    const controller = new AbortController();
    const timeoutId = setTimeout(() => controller.abort(), 3000);
    const response = await fetch('https://ipapi.co/json/', { 
      signal: controller.signal 
    });
    clearTimeout(timeoutId);
    if (response.ok) {
      const data = await response.json();
      return data.country_code || null;
    }
  } catch {
    // 忽略错误
  }
  return null;
};

export const LanguageProvider = ({ children }: { children: ReactNode }) => {
  const [language, setLanguage] = useState<Language>(() => {
    // 1. 优先使用本地保存的语言
    const saved = localStorage.getItem('tripcngo_lang');
    if (saved && ['zh', 'en', 'ja', 'ko', 'ru', 'fr', 'es', 'de', 'tw', 'it'].includes(saved)) {
      return saved as Language;
    }
    // 2. 检测浏览器语言设置
    return detectBrowserLanguage();
  });

  const [ipChecked, setIpChecked] = useState(false);

  // 基于 IP 检测语言（首次加载时）
  useEffect(() => {
    const saved = localStorage.getItem('tripcngo_lang');
    // 如果用户已经有保存的语言设置，不进行 IP 检测
    if (saved) {
      setIpChecked(true);
      return;
    }

    const checkIPAndSetLanguage = async () => {
      const countryCode = await detectIPCountry();
      if (countryCode && countryToLanguage[countryCode]) {
        setLanguage(countryToLanguage[countryCode]);
      }
      setIpChecked(true);
    };

    checkIPAndSetLanguage();
  }, []);

  useEffect(() => {
    localStorage.setItem('tripcngo_lang', language);
    document.documentElement.lang = language;
  }, [language]);

  const t = (key: string) => {
    return translations[language][key] || key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};
